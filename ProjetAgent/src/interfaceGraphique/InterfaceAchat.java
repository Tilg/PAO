    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:    	
    	clienBehavior.askProductWithSupplier(listeMessage.get(0).getReference());
    	ArrayList<Message> lMessage = clienBehavior.receivePriceProductFromSupplier();
		for (int i = 0; i < lMessage.size(); i++) {
			Message m = lMessage.get(i);
			m.setReference(listeMessage.get(0).getReference());
			m.setType("Fournisseur");
			this.addRowInJTable(lMessage.get(i));
		}
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        int indice = jTable1.getSelectedRow();
        System.out.println("Indice : "+indice);
        if(indice != -1){
        	if(listeMessage.get(indice).getType().equals("vendeur")){
        		clienBehavior.askToBuyProductWithSeller(listeMessage.get(indice).getReference());
            	Message mess = clienBehavior.receiveResponseFromSeller();
            	if(mess != null){
            		JOptionPane.showConfirmDialog(this, "Votre achat est valide");
            	}
        	}else{
        		clienBehavior.askToBuyProductWithSupplier(listeMessage.get(indice).getReference());
            	Message mess = clienBehavior.receiveResponseFromSupplier();
            	if(mess != null){
            		JOptionPane.showConfirmDialog(this, "Votre achat est valide");
            	}
        	}
        	
        }
    }   
    
