    public void addRowInJTable(Message message){
    	DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    	model.addRow(new Object[]{message.getReference(), message.getCategorie(), message.getPrix(), message.getVendeur(), message.getType()});
    }

